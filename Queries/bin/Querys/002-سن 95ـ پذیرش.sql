 /*  002  Sen95  */ 
IF COL_LENGTH('TABLENAME','Sen95') IS NULL BEGIN ALTER TABLE [TABLENAME] ADD Sen95 INT END
GO
IF COL_LENGTH('TABLENAME','Sen95') IS NOT NULL BEGIN UPDATE [TABLENAME] SET Sen95=NULL END
UPDATE a SET a.Sen95=b.Sen95 FROM [TABLENAME] a
JOIN  
	(	SELECT USERCODE CodePaziresh,1395-
			(CASE WHEN ISNUMERIC(SUBSTRING(Birthday,1,4))=1
			THEN CAST(SUBSTRING(Birthday,1,4) AS INT) ELSE NULL END) AS Sen95
		FROM HQ_Registery.dbo.STU_mainINFO
	)b ON b.CodePaziresh = a.CodePaziresh
